#include <stdio.h>

/**
 * op_add - addition function
 * @a: first param
 * @b: second param
 * Return: result
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtraction function
 * @a: first param
 * @b: second param
 * Return: result
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplication function
 * @a: first param
 * @b: second param
 * Return: result
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division function
 * @a: first param
 * @b: second param
 * Return: result
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modolus function
 * @a: first param
 * @b: second param
 * Return: result
*/

int op_mod(int a, int b)
{
	return (a % b);
}
